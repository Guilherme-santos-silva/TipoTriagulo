    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        //instanciando nossa classe "Controle" e dando os 3 parametros que ela precisa txflLado1, txflLado2 e txflLado3
        //usamos getText para ler os dados inseridos
        Modelo.Controle controle = new Modelo.Controle(txflLado1.getText(), txflLado2.getText(), txflLado3.getText());
        
        //inicimoas nosso controle com if, se o atributo mensagem estiver vazio 
        if(controle.getMensagem().equals(""))
        {
            //se estiver vazio entao setamos nosso lblResposta com o valor que vira da classe controle "controle.getResposta"
            lblResposta.setText(controle.getResposta());
        }
        //iniciando nosso else
        else 
        {
            //se nao estiver vazio entao criamos um "JOptionPane" e atribuimos nossa mensagem de erro vinda da classe controle "controle.getMensagem()"
            JOptionPane.showMessageDialog(null, controle.getMensagem());
        }
    }//GEN-LAST:event_btnVerificarActionPerformed

